void sound_management(data_t *texture)
{
    texture->sbang = sfSoundBuffer_createFromFile("sound/dinosaur.ogg");
    texture->bang = sfSound_create();
    sfSound_setBuffer(texture->bang, texture->sbang);
    sfSound_play(texture->bang);
}